package cs5004.animator.model.shapes.color;

/**
 * This is a rgb color interface.
 */
public interface Color {

  /**
   * Get the red value.
   *
   * @return the red value
   */
  int getRed();

  /**
   * Get the green value.
   *
   * @return the green value
   */
  int getGreen();

  /**
   * Get the blue value.
   *
   * @return the blue value
   */
  int getBlue();
}
